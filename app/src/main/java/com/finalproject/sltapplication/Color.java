    public String getRGBName(int r, int g, int b){

        ColorName colorMatch = null;
        int maxMSE = Integer.MAX_VALUE;
        int mse;
        for (ColorName color: colorNameArrayList){
            mse = color.getMSE(r, g, b);
            if (mse < maxMSE){
                maxMSE = mse;
                colorMatch = color;
            }
        }

        if (colorMatch != null){
            return colorMatch.getColorName();
        }
        else{
            return "Color name don't matched.";
        }
    }

    public class ColorName{
        public int r, g, b;
        public String name;

        public ColorName(String name, int r, int g, int b){
            this.name = name;
            this.r = r;
            this.g = g;
            this.b = b;
        }

        public int getMSE(int pixR, int pixG, int pixB){
            return (int) (((pixR-r)*(pixR-r) + (pixG-g)*(pixG-g) + (pixB-b)*(pixB-b))/3);
        }

        public String getColorName(){
            return name;
        }

        public int getR(){
            return r;
        }

        public int getG(){
            return g;
        }

        public int getB(){
            return b;
        }
    }
